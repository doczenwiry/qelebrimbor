logging.basicConfig(level=logging.INFO)

# Because matplotlib doesn't like the coloring ANSI escape sequences.
# Generated by Gemini (Google AI), May 2026
__ANSI_ESCAPE = re.compile(r"\x1b\[[0-9;]*m")

parser = ArgumentParser(
    prog="benchmark-dataset-rings",
    description="A tool to benchmark the current version of qelebrimbor against a fixed dataset of randomly generated ZX-graphs.",  # noqa: E501
)
parser.add_argument(
    "-a",
    "--analyse",
    action="store_true",
    help="perform feature analysis of the dataset.",
)
parser.add_argument("-c", "--comment", action="store", help="specify a comment to be appended to the filename")
parser.add_argument(
    "-d",
    "--dataset",
    action="store",
    choices=["small", "medio"],
    default="small",
    help="specify a dataset to be processed",
)
parser.add_argument(
    "-g",
    "--generate",
    action="store_true",
    help="only generate the dataset if it is not detected",
)
parser.add_argument(
    "-t",
    "--timeout",
    action="store",
    default=10,
    help="set the timeout to abort a run of qb.py.",
)
parser.add_argument(
    "-r",
    "--robust",
    action="store_true",
    help="enable robust run of the benchmark to increase the number of seeds from 10 to 100.",
)


def analyse(vzx: VolumetricZxGraph) -> dict[str, Any]:
    nxg = cast(networkx.Graph, vzx)

    planar, _ = networkx.check_planarity(nxg)

    if CycleAnalyser.has_cycles(vzx):
        largest_cycle = len(max(CycleAnalyser.decompose(vzx, minimal=True), key=lambda c: c.length))
    else:
        largest_cycle = None

    report: dict[str, Any] = {
        "CN": nxg.number_of_edges() - nxg.number_of_nodes() + networkx.number_connected_components(nxg),
        "LC": largest_cycle,
        "PL": planar,
        "kC": networkx.node_connectivity(nxg),
        "MD": max(nxg.degree[node] for node in nxg.nodes),
    }

    print(report)

    return report


def process(filepath: str) -> dict[str, Any] | None:
    report: dict[str, Any] | None = None
    try:
        outcome = subprocess.run(
            [sys.executable, "-m", "qb", "-cs", filepath],
            capture_output=True,
            timeout=arguments.timeout,
            text=True,
        )
        output: str = outcome.stdout
        print(output, end="")

        output = __ANSI_ESCAPE.sub("", output)
        if output.startswith("FAILURE"):
            report = {
                "qubits": int(qubits[1:]),
                "layers": int(layers[1:]),
                "seed": int(seed[1:]),
                "status": "FAILURE",
            }
        elif not output.startswith("ABORTED"):
            parts = output.split(",")
            report = {
                "qubits": int(qubits[1:]),
                "layers": int(layers[1:]),
                "seed": int(seed[1:]),
                "status": "COMPLETE" if float(parts[2].split(":")[1][:-1]) == 100.0 else "INCOMPLETE",
                "run": float(parts[0].split(":")[1][:-1]),
                "isc": int(parts[8].split(":")[1]),
                "itv": int(parts[9].split(":")[1]),
                "iir": float(parts[4].split(":")[1][:-1]),
                "ewi": parts[10].split(":")[1][:-1] == "SUCCESS",
            }

    except KeyboardInterrupt:
        print("Benchmarking interrupted by user.")

    except subprocess.CalledProcessError as e:
        # This catches the crash and prevents the main script from stopping
        print(f"FAILURE (exit:{e.returncode})")

    except subprocess.TimeoutExpired:
        print(f"ABORTED RUN [longer than {arguments.timeout} seconds].")
        report = {
            "qubits": int(qubits[1:]),
            "layers": int(layers[1:]),
            "seed": int(seed[1:]),
            "status": "ABORTED",
        }

    return report


if __name__ == "__main__":
    arguments = parser.parse_args()

    sample_size: str = "robust" if arguments.robust else "quick"

    dataset = Dataset[arguments.dataset.upper()]
    benchmark = Benchmark(dataset, robust=arguments.robust)
    print(f"Benchmarking dataset {dataset} [{'100' if arguments.robust else '10'} seeds per parameters]")

    if not benchmark.dataset_detected():
        print(f"Generating dataset {dataset} in {benchmark.directory}")
        benchmark.generate_dataset()
    else:
        print(f"Existing dataset found in {benchmark.directory}")

    if arguments.generate:
        exit(0)

    longest_file_name = max(map(len, benchmark.filenames))

    results: list[dict[str, Any]] = list()
    for input_path in benchmark.filenames:
        try:
            pyzx_internal = FullReduction.process(PYZX.from_file(benchmark.directory + "/" + input_path))
            pyzx_internal = BialgebraReduction.process(pyzx_internal)
            vzx = PYZX.from_pyzx_graph(pyzx_internal)
        except KeyError as ke:
            print(f"> {input_path.ljust(longest_file_name, ' ')}")
            raise ke

        cc_count = ConnectedComponentsAnalyser.count(vzx)
        bcc_count = BiconnectedComponentsAnalyser.count(vzx)
        if CycleAnalyser.has_cycles(vzx) and cc_count == 1 and bcc_count == 1:
            print(f"> {input_path.ljust(longest_file_name, ' ')} :", end=" ", flush=True)

            basename, _, _ = input_path.split(".")
            _, _, qubits, layers, seed = basename.split("-")
            filepath = f"{benchmark.directory}/{input_path}"

            result = analyse(vzx) if arguments.analyse else process(filepath)

            if result is not None:
                results.append(result)
            else:
                break

    commit = git.Repo().head.commit.hexsha[:8]

    runtype = "analysis" if arguments.analyse else "results"
    result_filename = f"benchmarking/results/benchmark-{runtype}-{dataset.name.lower()}-{sample_size}-{commit}"
    if arguments.comment:
        result_filename += f"-{arguments.comment}"
    result_filename += ".csv"
    with open(result_filename, "w") as file:
        data = pandas.DataFrame(results)
        data.index.name = "id"
        data.to_csv(file, encoding="utf-8")
        print(f"Results written to {file.name}")
